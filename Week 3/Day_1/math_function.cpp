#include<iostream>
#include<cmath>
#include<limits>
using namespace std;


int main()
{
    double x;
    cout<<"Enter the value: ";
    cin>>x;

    cout<<"abs("<<x<<"): "<<abs(x)<<endl;
    cout<<"acos("<<x<<"): "<<acos(x)<<endl;
    cout<<"ceil("<<x<<"): "<<ceil(x)<<endl;
    cout<<"log("<<x<<"): "<<log(x)<<endl;
}